package com.cmv.petGenesis.model;

public class ExamECOP {
	
	private int idEcop;
	private String weight;
	private String temperament;
	private String attitude;
	private String stool;
	private String apetitte;
	private String water;
	private String diet;
	private String nails;
	private String vomit;
	
	public ExamECOP() {
	}
	
	public ExamECOP(int idEcop) {
		this.idEcop = idEcop;
	}
	
	public int getIdEcop() {
		return idEcop;
	}
	public void setIdEcop(int idEcop) {
		this.idEcop = idEcop;
	}
	public String getWeight() {
		return weight;
	}
	public void setWeight(String weight) {
		this.weight = weight;
	}
	public String getTemperament() {
		return temperament;
	}
	public void setTemperament(String temperament) {
		this.temperament = temperament;
	}
	public String getAttitude() {
		return attitude;
	}
	public void setAttitude(String attitude) {
		this.attitude = attitude;
	}
	public String getStool() {
		return stool;
	}
	public void setStool(String stool) {
		this.stool = stool;
	}
	public String getApetitte() {
		return apetitte;
	}
	public void setApetitte(String apetitte) {
		this.apetitte = apetitte;
	}
	public String getWater() {
		return water;
	}
	public void setWater(String water) {
		this.water = water;
	}
	public String getDiet() {
		return diet;
	}
	public void setDiet(String diet) {
		this.diet = diet;
	}
	public String getNails() {
		return nails;
	}
	public void setNails(String nails) {
		this.nails = nails;
	}
	public String getVomit() {
		return vomit;
	}
	public void setVomit(String vomit) {
		this.vomit = vomit;
	}
	
}
